package modelo;

/**
 *
 * @author fraid
 */
public class Tripla <A, B, C>{
    public final A a;
    public final B b;
    public final C c;    
    /**
     * Constructor de una trupla
     * 
     */   
    public Tripla(A a, B b, C c) { 
        this.a = a; 
        this.b = b; 
        this.c = c;
      
    }
}
